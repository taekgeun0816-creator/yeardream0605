   [주의] SQLite 서브쿼리 주의사항
   ─────────────────────────────────────────────────────────────
   기능                | 지원 여부 | SQLite 대체 방법
   ─────────────────────────────────────────────────────────────
   단일 행 (=,>,<,>=,<=)|  지원  | 그대로 사용
   IN / NOT IN         |  지원  | 그대로 사용
   EXISTS / NOT EXISTS  |  지원  | 그대로 사용
   다중 컬럼 서브쿼리   |  지원  | (col1, col2) IN (서브쿼리)
